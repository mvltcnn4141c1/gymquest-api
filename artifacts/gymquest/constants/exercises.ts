export const EXERCISES_BY_CATEGORY: Record<ExerciseCategory, ExerciseDef[]> =
  ALL_EXERCISES.reduce(
    (acc, ex) => {
      if (!acc[ex.category]) acc[ex.category] = [];
      acc[ex.category].push(ex);
      return acc;
    },
    {} as Record<ExerciseCategory, ExerciseDef[]>,
  );

export const EXERCISE_MAP: Record<string, ExerciseDef> = Object.fromEntries(
  ALL_EXERCISES.map((ex) => [ex.id, ex]),
);

const DIFFICULTY_MULT: Record<ExerciseDifficulty, number> = {
  baslangic: 1.0,
  orta: 1.15,
  ileri: 1.3,
};

export function getExerciseXP(
  exId: string,
  sets: number,
  reps: number,
  duration: number,
  km: number,
): number {
  const ex = EXERCISE_MAP[exId];
  if (!ex) return 8;
  const dm = DIFFICULTY_MULT[ex.difficulty] ?? 1.0;
  if (ex.unit === "km") return Math.floor(ex.xpPerUnit * km * dm);
  if (ex.unit === "minutes") return Math.floor((ex.xpPerUnit * duration * dm) / 60);
  return Math.floor(ex.xpPerUnit * sets * Math.sqrt(reps) * dm);
}

export function getExerciseCal(
  exId: string,
  sets: number,
  reps: number,
  duration: number,
  km: number,
  weightKg = 75,
): number {
  const ex = EXERCISE_MAP[exId];
  if (!ex) return 0;
  if (ex.unit === "km") return Math.round(ex.met * weightKg * (km / 10));
  if (ex.unit === "minutes")
    return Math.round((ex.met * weightKg * duration) / 60);
  const estMin = (sets * reps * ex.tempoSec) / 60;
  return Math.round(ex.met * weightKg * estMin);
}
